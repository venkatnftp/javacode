
public class EquilateralTriangle extends Triangle{

	private int length = 0;

	public EquilateralTriangle(int length) {
		super();
		this.length = length;
	}

	@Override
	public double getArea() {
		return length;
	}

	@Override
	public double getA() {
		return length;
	}

	@Override
	public double getB() {
		return length;
	}

	@Override
	public double getC() {
		return length;
	}
	
	
}
